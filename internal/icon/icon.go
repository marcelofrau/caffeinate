var palette = []color.RGBA{
	{0, 0, 0, 0},         // 0 transparent
	{101, 55, 0, 255},    // 1 dark brown (outline/rim)
	{160, 100, 30, 255},  // 2 medium brown (cup body)
	{210, 155, 75, 255},  // 3 light brown (coffee fill)
	{220, 220, 220, 180}, // 4 light grey (steam)
	{80, 40, 0, 255},     // 5 very dark brown (saucer/shadow)
}

// PNG returns a 32×32 coffee cup icon as a PNG byte slice.
func PNG() []byte {
	img := image.NewRGBA(image.Rect(0, 0, 32, 32))
	for y := 0; y < 32; y++ {
		for x := 0; x < 32; x++ {
			img.SetRGBA(x, y, palette[coffeePixels[y][x]])
		}
	}
	var buf bytes.Buffer
	_ = png.Encode(&buf, img)
	return buf.Bytes()
}
